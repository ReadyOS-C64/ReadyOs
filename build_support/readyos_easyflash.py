def resolve_app_artifact(prg: str) -> Path:
    if prg == "readyshell":
        return BIN_DIR / "readyshell_easyflash.prg"
    return BIN_DIR / f"{prg}.prg"


def build_layout(catalog: Dict[str, object]) -> Dict[str, object]:
    next_bank = 1

    launcher_path = BIN_DIR / "launcher_easyflash.prg"
    launcher_len = payload_len_from_prg(launcher_path)
    launcher_load = payload_load_from_prg(launcher_path)
    if launcher_load != APP_LOAD_START:
        fail(f"launcher_easyflash load address changed: ${launcher_load:04X}")
    launcher_span = int(math.ceil(float(launcher_len) / float(EASYFLASH_PAYLOAD_SLOT_BYTES)))
    layout: Dict[str, object] = {
        "launcher": {
            "artifact": str(launcher_path.relative_to(ROOT)),
            "start_bank": next_bank,
            "bank_span": launcher_span,
            "load_addr": launcher_load,
            "payload_len": launcher_len,
            "checksum16": payload_checksum16_from_prg(launcher_path),
        },
        "apps": [],
        "overlays": [],
    }
    next_bank += launcher_span

    for entry in list(catalog["apps"]):
        app_path = resolve_app_artifact(str(entry["prg"]))
        payload_len = payload_len_from_prg(app_path)
        load_addr = payload_load_from_prg(app_path)
        if load_addr < APP_LOAD_START or load_addr >= (APP_LOAD_START + APP_SNAPSHOT_SIZE):
            fail(f"app load address out of range for {entry['prg']}: ${load_addr:04X}")
        bank_span = int(math.ceil(float(payload_len) / float(EASYFLASH_PAYLOAD_SLOT_BYTES)))
        layout["apps"].append({
            "bank": int(entry["bank"]),
            "prg": str(entry["prg"]),
            "label": str(entry["label"]),
            "artifact": str(app_path.relative_to(ROOT)),
            "start_bank": next_bank,
            "bank_span": bank_span,
            "load_addr": load_addr,
            "payload_len": payload_len,
            "checksum16": payload_checksum16_from_prg(app_path),
            "default_slot": int(entry["default_slot"]),
        })
        next_bank += bank_span

    for name, rel_path, reu_bank, reu_off, valid_bit in OVERLAY_SPECS:
        overlay_path = ROOT / rel_path
        payload_len = payload_len_from_prg(overlay_path)
        load_addr = payload_load_from_prg(overlay_path)
        if load_addr != READYSHELL_OVL_LOAD:
            fail(f"overlay load address changed for {name}: ${load_addr:04X}")
        bank_span = int(math.ceil(float(payload_len) / float(EASYFLASH_PAYLOAD_SLOT_BYTES)))
        layout["overlays"].append({
            "name": name,
            "artifact": str(overlay_path.relative_to(ROOT)),
            "start_bank": next_bank,
            "bank_span": bank_span,
            "load_addr": load_addr,
            "payload_len": payload_len,
            "checksum16": payload_checksum16_from_prg(overlay_path),
            "reu_bank": reu_bank,
            "reu_off": reu_off,
            "valid_bit": valid_bit,
        })
        next_bank += bank_span

    if next_bank > EASYFLASH_TOTAL_BANKS:
        fail(f"easyflash payload exceeds {EASYFLASH_TOTAL_BANKS - 1} data banks")
    layout["last_used_bank"] = next_bank - 1
    return layout


