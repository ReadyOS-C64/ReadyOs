EASYFLASH_BANK_BYTES = 0x4000
EASYFLASH_LOW_BYTES = 0x2000
EASYFLASH_PAYLOAD_SLOT_BYTES = 0x4000
EASYFLASH_TOTAL_BANKS = 64
EASYFLASH_TOTAL_BYTES = EASYFLASH_TOTAL_BANKS * EASYFLASH_BANK_BYTES
APP_LOAD_START = 0x1000
APP_SNAPSHOT_SIZE = 0xB600
READYSHELL_OVL_LOAD = 0x8E00
READYSHELL_OVL_SLOT_LEN = 0x3800
READYSHELL_OVL_META_OFF = 0x4880F0
READYSHELL_OVL_META_VERSION = 2
READYSHELL_OVL_META_VALID_MASK = 0xFF
READYSHELL_CACHE_BANK = 0x40
READYSHELL_CACHE_BANK2 = 0x41

OVERLAY_SPECS = (
    ("rsparser", "bin/rsparser.prg", READYSHELL_CACHE_BANK, 0x0000, 0x01),
    ("rsvm", "bin/rsvm.prg", READYSHELL_CACHE_BANK, 0x3800, 0x02),
    ("rsdrvilst", "bin/rsdrvilst.prg", READYSHELL_CACHE_BANK, 0x7000, 0x04),
    ("rsldv", "bin/rsldv.prg", READYSHELL_CACHE_BANK2, 0x0000, 0x08),
    ("rsstv", "bin/rsstv.prg", READYSHELL_CACHE_BANK, 0xA800, 0x10),
    ("rsfops", "bin/rsfops.prg", READYSHELL_CACHE_BANK2, 0x3800, 0x20),
    ("rscat", "bin/rscat.prg", READYSHELL_CACHE_BANK2, 0x7000, 0x40),
    ("rscopy", "bin/rscopy.prg", READYSHELL_CACHE_BANK2, 0xA800, 0x80),
)

REPORT_SYMBOLS = "123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz"


def fail(message: str) -> None:
    raise ValueError(message)


def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def run(cmd: List[str], *, cwd: Path | None = None) -> None:
    subprocess.run(cmd, cwd=str(cwd or ROOT), check=True, text=True)


def force_easyflash_subtype(crt_path: Path, subtype: int) -> None:
    data = bytearray(crt_path.read_bytes())
    if len(data) < 0x1B:
        fail(f"crt file too short to patch subtype: {crt_path}")
    data[0x1A] = subtype & 0xFF
    crt_path.write_bytes(bytes(data))
    verify = crt_path.read_bytes()
    if verify[0x1A] != (subtype & 0xFF):
        fail(f"failed to persist crt subtype patch for {crt_path}")


def c_escape(text: str) -> str:
    return text.replace("\\", "\\\\").replace('"', '\\"')


def parse_catalog(path: Path) -> Dict[str, object]:
    system_cfg, launcher_cfg, apps = apps_catalog.parse_source(str(path))
    entries: List[Dict[str, object]] = []
    for index, pair in enumerate(apps, start=1):
        entry_line, desc = pair
        _drive, prg, label, slot = apps_catalog.parse_app_entry(entry_line, str(path), index)
        entries.append({
            "bank": index,
            "prg": prg,
            "label": label,
            "desc": desc,
            "default_slot": int(slot or "0"),
        })
    return {
        "system": system_cfg,
        "launcher": launcher_cfg,
        "apps": entries,
    }


def payload_len_from_prg(path: Path) -> int:
    raw = path.read_bytes()
    if len(raw) < 3:
        fail(f"payload too short: {path}")
    return len(raw) - 2


def payload_load_from_prg(path: Path) -> int:
    raw = path.read_bytes()
    if len(raw) < 3:
        fail(f"payload too short: {path}")
    return raw[0] | (raw[1] << 8)


def payload_bytes_from_prg(path: Path) -> bytes:
    raw = path.read_bytes()
    if len(raw) < 3:
        fail(f"payload too short: {path}")
    return raw[2:]


def payload_checksum16_from_prg(path: Path) -> int:
    return sum(payload_bytes_from_prg(path)) & 0xFFFF


